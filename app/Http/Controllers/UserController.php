    public function update(UpdateUserRequest $request)
    {
        //認証しているユーザーを取得
        $user = \Auth::user();
        
        //フォーム画面で入力された値でレコードを更新
        $user->name = $request->name;
        $user->email = $request->email;
        $user->password = bcrypt($request->password);
        $user->save();
        
        return redirect()->route('details', ['id' => $user->id]);
    }

